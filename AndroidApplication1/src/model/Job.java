package model;

public class Job {
	
	private String Type;
	private String image;
	private String desc;
	public String getType() {
		return Type;
	}
	public void setType(String type) {
		Type = type;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	
	

}
